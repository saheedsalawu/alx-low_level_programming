int add(int, int);
int sub(int, int);
int mul(int, int);
int div(int, int);
int mod(int, int);

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a / b);
}

int mod(int a, int b)
{
	return (a % b);
}
